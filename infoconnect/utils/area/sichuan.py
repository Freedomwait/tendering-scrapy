    def export_json_tree(self):
        return self.data

    def district_and_city_list(self):
        result = []
        for item in self.data:
            children = item['children']
            for child in children:
                result.append((child["label"], item["label"]))

        return result

    def export_district_list(self):
        district_and_city = self.district_and_city_list()
        list = []
        for item in district_and_city:
            [district, city] = item
            list.append(district)
        return list

    def export_city(self, target_district):
        district_and_city = self.district_and_city_list()
        for item in district_and_city:
            [district, city] = item
            if (district == target_district):
                return city

    def export_province(self):
        return '四川省'

    def get_areainfo_by_value(self, target_value):
        result_area = {
            "province": self.export_province(),
            "county": '',
            "district": ''
        }
        for item in self.data:
            if item["regionCode"] == target_value:
                result_area["county"] = item["name"]
                break
            elif ('children' in item and len(item["children"])):
                for county_item in item["children"]:
                    if county_item["regionCode"] == target_value:
                        result_area["county"] = item["name"]
                        result_area["district"] = county_item["name"]
                        break

        return result_area


